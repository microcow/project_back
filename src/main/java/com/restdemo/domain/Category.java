package com.restdemo.domain;

public class Category {
	private String code;
	private String lgcategory;
	private String smcategory;
	private String level;
	
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getLgcategory() {
		return lgcategory;
	}
	public void setLgcategory(String lgcategory) {
		this.lgcategory = lgcategory;
	}
	public String getSmcategory() {
		return smcategory;
	}
	public void setSmcategory(String smcategory) {
		this.smcategory = smcategory;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
}
